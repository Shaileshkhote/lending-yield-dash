import { readdir, mkdir, writeFile } from "node:fs/promises";
import { basename, join } from "node:path";

const root = new URL("..", import.meta.url);
const dataDir = new URL("src/data/", root);
const outputDir = new URL("src/generated/", root);
const outputFile = new URL("protocols.ts", outputDir);

const files = (await readdir(dataDir))
  .filter((file) => file.endsWith(".json") && file !== "chains.json")
  .sort();

const imports = files.map((file, index) => {
  const name = `protocol${index}`;
  return `import ${name} from "../data/${file}";`;
});

const items = files.map((_file, index) => `  protocol${index},`);
const source = `// Auto-generated by scripts/generate-registry.mjs. Do not edit manually.
${imports.join("\n")}

export const protocolDataFiles = [
${items.join("\n")}
];
`;

await mkdir(outputDir, { recursive: true });
await writeFile(outputFile, source);

console.log(`Generated ${basename(outputFile.pathname)} from ${files.length} protocol JSON files.`);
